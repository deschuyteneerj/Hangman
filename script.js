//Canvas creation:
let canvas =  function(){
    hangman = document.getElementById("hangman");
    context = hangman.getContext('2d');
    context.strokeStyle = "black";
    context.fillStyle = "#fff";
    context.lineWidth = 3;
    context.beginPath();
};

class HangmanParts {
    constructor(x, y, toX, toY, a=false) {
        this.x = x;
        this.y = y;
        this.toX = toX;
        this.toY = toY;
        if (a) this.circle=true;
    }

    draw() {
        if (this.circle) {
            context.beginPath();
            context.arc(this.x, this.y, this.toX, this.toY, Math.PI*2, true);
            context.strokeStyle = "black";
            context.stroke();
        }
        else {
            context.beginPath();
            context.moveTo(this.x, this.y);
            context.lineTo(this.toX, this.toY);
            context.strokeStyle = "black";
            context.stroke();
        }
    }
}

canvas();

